(
  PKG_ID           NUMBER(7),
  PKG_NAME         VARCHAR2(255 BYTE),
  STATUS           NUMBER(1),
  GROUP_ID         NUMBER(7),
  MAX_SMS_PER_PKG  NUMBER(2)                    DEFAULT 4,
  CP_ID            NUMBER(30)
)
TABLESPACE MTPKG
RESULT_CACHE (MODE DEFAULT)
PCTUSED    0
