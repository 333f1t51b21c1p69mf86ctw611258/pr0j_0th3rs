(
  ID            NUMBER(6)                       NOT NULL,
  NAME          VARCHAR2(50 BYTE),
  PHONE         VARCHAR2(30 CHAR),
  EMAIL         VARCHAR2(50 CHAR),
  STATUS        NUMBER(1),
  DESCRIPTION   VARCHAR2(2000 BYTE),
  TIME_CREATED  DATE,
  USER_CREATED  VARCHAR2(100 BYTE),
  TO_CHANEL     NUMBER(4),
  LEVEL_ID      VARCHAR2(200 BYTE)
)
