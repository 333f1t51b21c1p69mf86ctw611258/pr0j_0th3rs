  DISCOUNT       NUMBER(9,5),
  MSISDNS        VARCHAR2(15 BYTE),
  CHARG_TYPE     NUMBER(1)                      NOT NULL,
  PKGD_ID        VARCHAR2(127 BYTE)             NOT NULL,
  RATE_DISCOUNT  NUMBER(5,2)
)
