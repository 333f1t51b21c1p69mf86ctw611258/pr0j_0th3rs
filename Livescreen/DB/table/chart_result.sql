DROP TABLE LIVESCREEN.CHART_RESULT CASCADE CONSTRAINTS;

CREATE GLOBAL TEMPORARY TABLE LIVESCREEN.CHART_RESULT
(
  RESULT_TIME    VARCHAR2(23 BYTE),
  RESULT_NAME    VARCHAR2(1023 BYTE),
  RESULT_COUNT   NUMBER(9),
  RESULT_AMOUNT  NUMBER,
  RESULT_ID      NUMBER(9)                      NOT NULL
)
ON COMMIT DELETE ROWS
RESULT_CACHE (MODE DEFAULT)
NOCACHE;


CREATE OR REPLACE PUBLIC SYNONYM CHART_RESULT FOR LIVESCREEN.CHART_RESULT;
